public class Primitive {
    public static void main(String[] args) {
        float f=10.5f;
        System.out.print("Value of float is " + f);
        double d=1444040.123456789;
        System.out.print("\nValue of double is " + d);
        char c='A';
        System.out.print("\nValue of char is " + c);
        long l=1234567890123456789L;
        System.out.print("\nValue of long is " + l);
        short s = 1000;
        System.out.print("\nValue of short is " + s);
        System.out.print("\nRange of short: " + Short.MIN_VALUE + " to " + Short.MAX_VALUE);
        int i = 100000;
        System.out.print("\nValue of int is " + i);
        System.out.print("\nRange of int: " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
        System.out.print("\nRange of long: " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);
        
    }
    
}
